class Solution
{
public:
    vector<int> subsetSums(vector<int> arr, int N)
    {
        vector<int> res;
        for(int num=0; num<pow(2,N); num++) {
            int sum = 0;
            for(int i=0; i<N; i++) {
                if(num & (1<<i)) {
                    sum += arr[i];
                }
            }
            res.push_back(sum);
        }
        return res;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin>>N;
        vector<int> arr(N);
        for(int i = 0 ; i < N ; i++){
            cin >> arr[i];
        }
        Solution ob;
        vector<int> ans = ob.subsetSums(arr,N);
        sort(ans.begin(),ans.end());
        for(auto sum : ans){
            cout<< sum<<" ";
        }
        cout<<endl;
    }
    return 0;
}
